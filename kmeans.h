/**
 * @file kmeans.h
 * @brief Заголовочный файл модуля кластеризации алгоритма K-means и K-means++.
 * * В данном файле определены основные интерфейсы и структуры данных, необходимые
 * для инициализации центроидов и выполнения итерационного процесса кластеризации.
 * Разделение на заголовочный файл позволяет использовать функции кластеризации
 * в любых других модулях проекта без дублирования кода.
 */

#ifndef KMEANS_H
#define KMEANS_H

/**
 * @brief Функция умной инициализации центроидов методом K-means++.
 * * Данный метод выбирает начальные центроиды на основе распределения вероятностей:
 * первый центр выбирается случайно, а каждый следующий — с вероятностью, 
 * пропорциональной квадрату расстояния до ближайшего существующего центроида.
 * Это предотвращает проблему близкого расположения начальных центроидов.
 * * @param data Указатель на массив исходных данных (размерность n_points * n_features).
 * @param n_points Общее количество точек в датасете.
 * @param n_features Количество признаков (размерность пространства, например, 2 для X и Y).
 * @param k Требуемое количество кластеров.
 * @param centroids Выходной массив для сохранения координат найденных центроидов (размер k * n_features).
 */
void init_centroids_kmeans_pp(const double* data, int n_points, int n_features, int k, double* centroids);

/**
 * @brief Основная функция выполнения алгоритма кластеризации K-means.
 * * Функция запускает итерационный процесс: распределение точек по кластерам и 
 * пересчет центроидов до тех пор, пока метки кластеров не перестанут изменяться
 * или не будет достигнуто максимальное число итераций. Внутри себя использует K-means++.
 * * @param data Указатель на плоский массив данных (размер n_points * n_features).
 * @param n_points Количество точек.
 * @param n_features Количество признаков у каждой точки.
 * @param k Количество кластеров для поиска.
 * @param max_iters Ограничение на максимальное количество шагов алгоритма.
 * @param labels Выходной массив для сохранения итоговых меток точек (размер n_points).
 * @param centroids Выходной массив для сохранения итоговых центроидов (размер k * n_features).
 * @return double Возвращает финальное значение внутренней инерции (WSS) для оценки качества.
 */
double run_kmeans(const double* data, int n_points, int n_features, int k, int max_iters, int* labels, double* centroids);

#endif // KMEANS_H