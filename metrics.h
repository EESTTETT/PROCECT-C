/**
 * @file metrics.h
 * @brief Заголовочный файл модуля расчета метрик качества внутренней кластеризации.
 * * Описывает функции для проведения математической оценки качества работы K-means
 * без использования внешних эталонных меток. Позволяет реализовать автоматический
 * выбор оптимального количества кластеров.
 */

#ifndef METRICS_H
#define METRICS_H

/**
 * @brief Функция расчета среднего силуэтного коэффициента (Silhouette Score) датасета.
 * * Силуэт измеряет, насколько каждая точка близка к своему кластеру и далека от соседних.
 * Значение метрики лежит в диапазоне [-1, 1]. Чем ближе к 1, тем качественнее разбиение.
 * * @param data Плоский массив координат точек (n_points * n_features).
 * @param labels Массив предсказанных алгоритмом меток кластеров для каждой точки.
 * @param n_points Общее количество точек.
 * @param n_features Количество признаков.
 * @param k Количество кластеров.
 * @return double Средний силуэтный коэффициент всего набора данных.
 */
double calculate_silhouette(const double* data, const int* labels, int n_points, int n_features, int k);

#endif // METRICS_H